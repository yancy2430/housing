  /**
   * 生命周期函数--监听页面显示
   */
  onShow: function () {
    let that = this;
    this.setData({
      token: wx.getStorageSync("user").token
    })
    //发起网络请求
    wx.request({
      url: 'https://weixin.tdeado.com/miniapp/me/qrcode',
      header: {
        'token': that.data.token,
        'content-type': 'application/json' // 默认值
      },
      success(res) {
        that.setData({
          qrcode: "https://weixin.tdeado.com"+res.data.data
        })
      }
    })
  },

